public class Attack {
    String name;
    int dmg;


    public Attack(String nameIn, int dmgIn){

        name = nameIn;
        dmg = dmgIn;

    }

    public String getName() {
        return name;
    }

    public int getDmg() {
        return dmg;
    }


}







